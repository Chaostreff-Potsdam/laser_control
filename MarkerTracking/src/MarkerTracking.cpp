MarkerTracking::MarkerTracking()
{
	m_detail = new MarkerTrackingDetail;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

MarkerTracking::~MarkerTracking()
{
	delete m_detail;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

int MarkerTracking::run(int argc, char *argv[])
{
	return m_detail->run(argc, argv);
}

