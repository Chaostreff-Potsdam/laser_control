#include "NetworkServer.h"
#include "NetworkServerDetail.h"

////////////////////////////////////////////////////////////////////////////////////////////////////

namespace network
{

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// NetworkServer
//
////////////////////////////////////////////////////////////////////////////////////////////////////

NetworkServer::NetworkServer(int port)
{
	m_detail = new NetworkServerDetail(port);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

NetworkServer::~NetworkServer()
{
	delete m_detail;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void NetworkServer::sendMarkerInformation(const markerTracking::MarkerPtr marker)
{
	m_detail->sendMarkerInformation(marker);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void NetworkServer::poll()
{
	m_detail->poll();
}

////////////////////////////////////////////////////////////////////////////////////////////////////

}
